#pragma once

#include <string>
#include <stdio.h>
#include <sstream>
#include <format>

using namespace std;

typedef enum
{
	ANSWER_CODE_OK,
	ANSWER_CODE_NAN,
	ANSWER_CODE_RANGE,
	ANSWER_CODE_NIN // Not Integer Number
} ANSWER_CODE_T;

enum NumberSystem
{
	Bin = 0,
	Oct,
	Dec,
	Hex
};

class Number
{
private:
	bool datatype = 0; // 0 - Int ; 1 - Float
	int IntValue = 0;
	double FloatValue = 0.0;
	NumberSystem NumSystem = Bin;
public:

	///////////////////////////////////////////////////////////
	// Get a string of number in Binary system
	///////////////////////////////////////////////////////////
	// OUTPUT:
	//		String contains an number in Binary system
	string GetBin();
	
	///////////////////////////////////////////////////////////
	//  Converts and save binary number string to value
	///////////////////////////////////////////////////////////
	//		
	// INPUT:
	//		input - string containing binnary number
	// OUTPUT:
	//		error codes or OK
	ANSWER_CODE_T WriteBin(string input);

	///////////////////////////////////////////////////////////
	// Get a string of number in Octal system
	///////////////////////////////////////////////////////////
	// OUTPUT:
	//		String contains an number in Octal system
	string GetOct();

	///////////////////////////////////////////////////////////
	//  Converts and save octal number string to value
	///////////////////////////////////////////////////////////
	//		
	// INPUT:
	//		input - string containing octal number
	// OUTPUT:
	//		error codes or OK
	ANSWER_CODE_T WriteOct(string input);

	///////////////////////////////////////////////////////////
	// Get a string of number in HEX system
	///////////////////////////////////////////////////////////
	// OUTPUT:
	//		String contains an number in HEX system
	string GetHex();

	///////////////////////////////////////////////////////////
	//  Converts and save HEX number string to value
	///////////////////////////////////////////////////////////
	//		
	// INPUT:
	//		input - string containing HEX number
	// OUTPUT:
	//		error codes or OK
	ANSWER_CODE_T WriteHex(string input);

	///////////////////////////////////////////////////////////
	// Get a string of number in Decimal system
	///////////////////////////////////////////////////////////
	// OUTPUT:
	//		String contains an number in Decimal system
	string GetDec();

	///////////////////////////////////////////////////////////
	//  Converts and save decimal number string to value
	///////////////////////////////////////////////////////////
	//		
	// INPUT:
	//		input - string containing decimal number
	// OUTPUT:
	//		error codes or OK
	ANSWER_CODE_T WriteDec(string input);

	///////////////////////////////////////////////////////////
	//  Converts and save selected system number string to value
	///////////////////////////////////////////////////////////
	//		
	// INPUT:
	//		input - string containing number in selected system
	// OUTPUT:
	//		error codes or OK
	ANSWER_CODE_T WriteDef(string input);

	///////////////////////////////////////////////////////////
	// Get a string of number in selected system
	///////////////////////////////////////////////////////////
	// OUTPUT:
	//		String contains an number in selected system
	string GetDef();

	///////////////////////////////////////////////////////////
	//  Save given floating point value 
	///////////////////////////////////////////////////////////
	//		
	// INPUT:
	//		Value - floating point value
	// OUTPUT:
	//		error codes or OK
	ANSWER_CODE_T WriteFloatValue(double Value);

	///////////////////////////////////////////////////////////
	//  Save given integer value 
	///////////////////////////////////////////////////////////
	//		
	// INPUT:
	//		Value - integer value
	// OUTPUT:
	//		error codes or OK
	ANSWER_CODE_T WriteIntValue(int Value);

	///////////////////////////////////////////////////////////
	//  Get current selected number system
	///////////////////////////////////////////////////////////
	//		
	// OUTPUT:
	//		Number system
	NumberSystem GetSystem();

	///////////////////////////////////////////////////////////
	//  Get floating point value
	///////////////////////////////////////////////////////////
	//		
	// OUTPUT:
	//		Floating point value
	double GetFloat();

	///////////////////////////////////////////////////////////
	//  Get integer value
	///////////////////////////////////////////////////////////
	//		
	// OUTPUT:
	//		integer value
	int GetInt();

	///////////////////////////////////////////////////////////
	// Set current number system
	///////////////////////////////////////////////////////////
	//		
	// INPUT:
	//		NSys - name of number system as NumberSystem enum
	// OUTPUT:
	//		void
	void SetDefNumSystem(NumberSystem NSys);

	///////////////////////////////////////////////////////////
	// Set number types - floating point or integer
	///////////////////////////////////////////////////////////
	//		
	// INPUT:
	//		n - 0 (integer) or 1 (floating point) 
	// OUTPUT:
	//		void
	void SetDataType(bool n);

};
